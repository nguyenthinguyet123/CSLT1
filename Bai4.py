a=int(input())
b=int(input())
c=int(input())
t=float((a*2+b*3+c)/6)
if(t<3):
    print("Kem")
elif(3<=t<5):
    print("Yeu")
elif(5<=t<6):
    print("Trung binh")
elif(6<=t<7):
    print("Trung binh Kha")
elif(7<=t<8):
    print("Kha")
elif(8<=t<9):
    print("Gioi")
elif(9<=t<10):
    print("Xuat sac")
